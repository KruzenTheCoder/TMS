create table if not exists public.authorized_students (
  id bigint generated by default as identity primary key,
  name text not null,
  class_name text not null,
  created_at timestamptz not null default now(),
  constraint authorized_students_class_check check (class_name in ('12A','12B','12C','12D'))
);

create unique index if not exists authorized_students_name_class_idx
  on public.authorized_students (name, class_name);

alter table public.authorized_students enable row level security;

drop policy if exists authorized_students_read_all on public.authorized_students;
create policy authorized_students_read_all on public.authorized_students
  for select
  using (true);

